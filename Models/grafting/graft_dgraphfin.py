# ─────────────────────────────────────────────────────────────────────────────

def validate_graph_cols(ieee_df: pd.DataFrame) -> pd.DataFrame:
    """
    The schema already contains pre-computed DGraphFin-derived columns:
      shared_infrastructure_count  — number of users sharing same IP/device
      2nd_degree_fraud_rate        — fraud rate in 2-hop neighborhood
      gnn_node_embedding           — already parsed in graft_ieee_cis
      graph_risk_score             — scalar summarising graph risk

    This function validates ranges, fills missing values with safe defaults,
    and adds derived interaction features.
    """
    # shared_infrastructure_count: int, clip to [0, 100]
    if "shared_infrastructure_count" in ieee_df.columns:
        ieee_df["shared_infrastructure_count"] = (
            ieee_df["shared_infrastructure_count"]
            .fillna(0).clip(0, 100).astype(np.int32)
        )
    else:
        ieee_df["shared_infrastructure_count"] = 0

    # 2nd_degree_fraud_rate: float in [0, 1]
    if "2nd_degree_fraud_rate" in ieee_df.columns:
        ieee_df["2nd_degree_fraud_rate"] = (
            ieee_df["2nd_degree_fraud_rate"]
            .fillna(0.0).clip(0.0, 1.0).astype(np.float32)
        )
    else:
        ieee_df["2nd_degree_fraud_rate"] = 0.0

    # graph_risk_score: float, usually [0,1] but normalise to be safe
    if "graph_risk_score" in ieee_df.columns:
        grs = ieee_df["graph_risk_score"].fillna(0.0)
        mn, mx = grs.min(), grs.max()
        ieee_df["graph_risk_score"] = ((grs - mn) / max(mx - mn, 1e-9)).astype(np.float32)
    else:
        ieee_df["graph_risk_score"] = 0.0

    # ── Derived interaction features ──
    # High shared infra AND high 2nd-degree fraud = strong ring signal
    ieee_df["ring_signal"] = (
        ieee_df["shared_infrastructure_count"].clip(0,10) / 10.0 *
        ieee_df["2nd_degree_fraud_rate"]
    ).astype(np.float32)

    # Combinatorial: high graph risk AND high transaction amount = escalated risk
    ieee_df["graph_amt_risk"] = (
        ieee_df["graph_risk_score"] *
        np.log1p(ieee_df["TransactionAmt"].fillna(0))
    ).astype(np.float32)

    print("[DGraphFin] Graph columns validated and interaction features added")
    return ieee_df


def get_transfer_weights(pretrained_path: str) -> Dict[str, torch.Tensor]:
    """
    Load the pre-trained DGraphFin weights.
    Strip the classification head — only encoder layers transfer.
    Called by Model E (HeteroGNN) during __init__ to initialise its
    shared encoder backbone.
    """
    state_dict = torch.load(pretrained_path, map_location="cpu")
    # Keep only layer1, layer2, layer3 weights (not classifier)
    encoder_weights = {
        k: v for k, v in state_dict.items()
        if not k.startswith("classifier")
    }
    print(f"[DGraphFin] Transfer weights loaded: {len(encoder_weights)} parameter tensors")
    return encoder_weights


