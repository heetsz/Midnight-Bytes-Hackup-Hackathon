"""
graft_dgraphfin.py
──────────────────
DGraphFin is a financial transaction graph from Alipay.
Its entity IDs have NO overlap with IEEE-CIS.

Two-stage graft:
  Stage A — Pre-training:
    Train GraphSAGE / HGT on DGraphFin's labeled graph.
    Save weights. These capture what "fraud neighborhood structure"
    looks like in a real financial graph.

  Stage B — Weight transfer + feature transfer:
    Initialize the IEEE-CIS GNN (Model E) with DGraphFin weights.
    Also use the pre-computed columns (shared_infrastructure_count,
    2nd_degree_fraud_rate, gnn_node_embedding, graph_risk_score)
    that were generated by running DGraphFin's GNN on IEEE-CIS users.
    These are already in the schema — we parse and validate them here.
"""

